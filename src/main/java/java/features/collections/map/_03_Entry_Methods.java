package java.features.collections.map;

public class _03_Entry_Methods {

    public static void main(String[] args) {

        /*
            Entry Interface
            ===============
            A map is a group of key-value pairs.Each key-value is called as an Entry.Hence, Map is
            considered as a collection of Entry objects.Without existence of Map object, there is
            no chance of existing of Entry object.Hence, Entry Interface is defined inside Map Interface.

            interface Map {

                interface Entry {
                    Object getKey();
                    Object getValue();
                    Object setValue(Object newObj);
                }
            }

            The above 3 methods are Entry-specific methods, and we can apply only on Entry objects.
        */
    }

}
